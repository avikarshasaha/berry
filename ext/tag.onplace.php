function tag_onplace($attr){
	$attr = array_merge(array(
	    'id' => 'onplace'
	), $attr);

	b::l('_session.tag.onplace', $attr);
	return tags::parse(tags::fill('span', array_merge($attr, array('id' => 'ajax['.$attr['id'].']'))));
}

